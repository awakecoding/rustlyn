    handle: i32,
}

impl ManagedString {
    fn from_handle(handle: i32) -> Self {
        Self { handle }
    }

    pub fn handle(&self) -> i32 {
        self.handle
    }

    pub unsafe fn from_borrowed_handle(handle: i32) -> Self {
        Self { handle }
    }

    pub fn into_handle(self) -> i32 {
        self.handle
    }

