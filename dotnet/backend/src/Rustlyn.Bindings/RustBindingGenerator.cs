using System.Text;

namespace Rustlyn.Bindings;

public static class RustBindingGenerator
{
    public static string GenerateSystemModule(BindingSurface surface)
    {
        ArgumentNullException.ThrowIfNull(surface);

        foreach (var requirement in surface.Requirements)
        {
            requirement.Validate();
        }

        foreach (var enumProjection in surface.RustEnumProjections)
        {
            enumProjection.Validate();
        }

        var builder = new StringBuilder();
        builder.AppendLine("// @generated by Rustlyn.Bindings. Do not edit by hand.");
        builder.AppendLine();
        builder.AppendLine("unsafe extern \"C\" {");
        foreach (var binding in surface.Externs)
        {
            foreach (var signatureLine in binding.SignatureLines)
            {
                builder.Append("    ");
                builder.AppendLine(signatureLine);
            }
        }

        builder.AppendLine("}");
        builder.AppendLine();
        builder.Append(GenerateRustEnumDefinitions(surface.RustEnumProjections));
        builder.Append(GenerateRustWrapperBody(surface));
        return builder.ToString();
    }

    private static string GenerateRustEnumDefinitions(IReadOnlyList<RustEnumProjection> enumProjections)
    {
        var builder = new StringBuilder();
        foreach (var enumProjection in enumProjections)
        {
            enumProjection.Validate();
            if (enumProjection.IsFlags)
            {
                AppendRustFlagsDefinition(builder, enumProjection);
                continue;
            }

            builder.AppendLine("#[repr(i32)]");
            builder.AppendLine("#[derive(Clone, Copy, Debug, Eq, PartialEq)]");
            builder.Append("pub enum ");
            builder.Append(enumProjection.RustName);
            builder.AppendLine(" {");
            foreach (var variant in enumProjection.Variants)
            {
                builder.Append("    ");
                builder.Append(variant.Name);
                builder.Append(" = ");
                builder.Append(variant.Value.ToString(System.Globalization.CultureInfo.InvariantCulture));
                builder.AppendLine(",");
            }

            builder.AppendLine("}");
            builder.AppendLine();
            builder.Append("impl ");
            builder.Append(enumProjection.RustName);
            builder.AppendLine(" {");
            builder.AppendLine("    pub fn as_i32(self) -> i32 {");
            builder.AppendLine("        self as i32");
            builder.AppendLine("    }");
            builder.AppendLine("}");
            builder.AppendLine();
        }

        return builder.ToString();
    }

    private static void AppendRustFlagsDefinition(StringBuilder builder, RustEnumProjection enumProjection)
    {
        builder.AppendLine("#[derive(Clone, Copy, Debug, Eq, PartialEq)]");
        builder.Append("pub struct ");
        builder.Append(enumProjection.RustName);
        builder.AppendLine(" {");
        builder.AppendLine("    bits: i32,");
        builder.AppendLine("}");
        builder.AppendLine();
        builder.AppendLine("#[allow(non_upper_case_globals)]");
        builder.Append("impl ");
        builder.Append(enumProjection.RustName);
        builder.AppendLine(" {");
        foreach (var variant in enumProjection.Variants)
        {
            builder.Append("    pub const ");
            builder.Append(variant.Name);
            builder.Append(": Self = Self { bits: ");
            builder.Append(variant.Value.ToString(System.Globalization.CultureInfo.InvariantCulture));
            builder.AppendLine(" };");
        }

        builder.AppendLine();
        builder.AppendLine("    pub fn union(self, other: Self) -> Self {");
        builder.AppendLine("        Self { bits: self.bits | other.bits }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    pub fn as_i32(self) -> i32 {");
        builder.AppendLine("        self.bits");
        builder.AppendLine("    }");
        builder.AppendLine("}");
        builder.AppendLine();
    }

    private static string GenerateRustWrapperBody(BindingSurface surface)
    {
        var bindings = surface.ManagedGlueBindings.ToDictionary(static binding => binding.Symbol, StringComparer.Ordinal);
        return RustWrapperBodyTemplate.Replace(
            "{{CALLBACK_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.Callback, "    "),
            StringComparison.Ordinal).Replace(
            "{{EXCEPTION_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.Exception, "    "),
            StringComparison.Ordinal).Replace(
            "{{CONSOLE_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.Console, "    "),
            StringComparison.Ordinal).Replace(
            "{{ENVIRONMENT_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.Environment, "    "),
            StringComparison.Ordinal).Replace(
            "{{MATH_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.Math, "    "),
            StringComparison.Ordinal).Replace(
            "{{MATHF_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.MathF, "    "),
            StringComparison.Ordinal).Replace(
            "{{TIME_SPAN_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.TimeSpan, "    "),
            StringComparison.Ordinal).Replace(
            "{{DATE_TIME_OFFSET_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.DateTimeOffset, "    "),
            StringComparison.Ordinal).Replace(
            "{{GUID_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.Guid, "    "),
            StringComparison.Ordinal).Replace(
            "{{IO_DIRECTORY_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.IoDirectory, "        "),
            StringComparison.Ordinal).Replace(
            "{{IO_FILE_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.IoFile, "        "),
            StringComparison.Ordinal).Replace(
            "{{IO_PATH_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.IoPath, "        "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_STRING_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedString, "    "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_STRING_ARRAY_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedStringArray, "    "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_INT_ARRAY_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedIntArray, "    "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_BYTE_ARRAY_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedByteArray, "    "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_TIME_SPAN_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedTimeSpan, "    "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_DATE_TIME_OFFSET_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedDateTimeOffset, "    "),
            StringComparison.Ordinal).Replace(
            "{{MANAGED_GUID_METHODS}}",
            GenerateRustWrapperMethods(surface.RustWrapperMethods, bindings, RustWrapperContainer.ManagedGuid, "    "),
            StringComparison.Ordinal);
    }

    private static string GenerateRustWrapperMethods(
        IReadOnlyList<RustWrapperMethod> methods,
        IReadOnlyDictionary<string, ManagedGlueBinding> bindings,
        RustWrapperContainer container,
        string indent)
    {
        var builder = new StringBuilder();
        var selectedMethods = methods.Where(method => method.Container == container).ToArray();
        for (var index = 0; index < selectedMethods.Length; index++)
        {
            if (index > 0)
            {
                builder.AppendLine();
            }

            var method = selectedMethods[index];
            if (!bindings.TryGetValue(method.ExternSymbol, out var binding))
            {
                throw new InvalidOperationException($"Rust wrapper method '{method.Signature}' targets unknown extern symbol '{method.ExternSymbol}'.");
            }

            AppendRustWrapperMethod(builder, method, binding, indent);
        }

        return builder.ToString().TrimEnd('\r', '\n');
    }

    private static void AppendRustWrapperMethod(StringBuilder builder, RustWrapperMethod method, ManagedGlueBinding binding, string indent)
    {
        builder.Append(indent);
        builder.Append(method.Signature);
        builder.AppendLine(" {");

        if (binding.Operation.ExceptionConvention == ManagedGlueExceptionConvention.ReturnExceptionHandle)
        {
            AppendReturnExceptionHandleWrapper(builder, method, indent);
        }
        else if (binding.Operation.ExceptionConvention == ManagedGlueExceptionConvention.WriteExceptionOut)
        {
            AppendWriteExceptionOutWrapper(builder, method, indent);
        }
        else
        {
            throw new NotSupportedException($"Managed glue exception convention '{binding.Operation.ExceptionConvention}' is not supported.");
        }

        builder.Append(indent);
        builder.AppendLine("}");
    }

    private static void AppendWriteExceptionOutWrapper(StringBuilder builder, RustWrapperMethod method, string indent)
    {
        builder.Append(indent);
        builder.AppendLine("    let mut exception_handle = 0;");
        builder.Append(indent);
        builder.Append("    let ");
        builder.Append(method.ResultVariableName);
        builder.AppendLine(" = unsafe {");
        builder.Append(indent);
        builder.Append("        ");
        builder.Append(GetRustWrapperSymbolAccess(method));
        builder.AppendLine("(");
        foreach (var argument in method.Arguments)
        {
            builder.Append(indent);
            builder.Append("            ");
            builder.Append(argument);
            builder.AppendLine(",");
        }

        builder.Append(indent);
        builder.AppendLine("            &mut exception_handle,");
        builder.Append(indent);
        builder.AppendLine("        )");
        builder.Append(indent);
        builder.AppendLine("    };");
        builder.Append(indent);
        builder.AppendLine("    Exception::from_handle(exception_handle)?;");
        builder.Append(indent);
        builder.Append("    ");
        builder.AppendLine(FormatRustWrapperSuccess(method));
    }

    private static void AppendReturnExceptionHandleWrapper(StringBuilder builder, RustWrapperMethod method, string indent)
    {
        builder.Append(indent);
        builder.AppendLine("    let exception_handle = unsafe {");
        builder.Append(indent);
        builder.Append("        ");
        builder.Append(GetRustWrapperSymbolAccess(method));
        builder.AppendLine("(");
        foreach (var argument in method.Arguments)
        {
            builder.Append(indent);
            builder.Append("            ");
            builder.Append(argument);
            builder.AppendLine(",");
        }

        builder.Append(indent);
        builder.AppendLine("        )");
        builder.Append(indent);
        builder.AppendLine("    };");
        builder.Append(indent);
        builder.AppendLine("    Exception::from_handle(exception_handle)?;");
        builder.Append(indent);
        builder.Append("    ");
        builder.AppendLine(FormatRustWrapperSuccess(method));
    }

    private static string GetRustWrapperSymbolAccess(RustWrapperMethod method)
    {
        return method.Container switch
        {
            RustWrapperContainer.Console => $"super::{method.ExternSymbol}",
            RustWrapperContainer.Callback => $"super::{method.ExternSymbol}",
            RustWrapperContainer.Environment => $"super::{method.ExternSymbol}",
            RustWrapperContainer.Exception => method.ExternSymbol,
            RustWrapperContainer.Math => $"super::{method.ExternSymbol}",
            RustWrapperContainer.MathF => $"super::{method.ExternSymbol}",
            RustWrapperContainer.TimeSpan => $"super::{method.ExternSymbol}",
            RustWrapperContainer.DateTimeOffset => $"super::{method.ExternSymbol}",
            RustWrapperContainer.Guid => $"super::{method.ExternSymbol}",
            RustWrapperContainer.IoDirectory => $"super::super::{method.ExternSymbol}",
            RustWrapperContainer.IoFile => $"super::super::{method.ExternSymbol}",
            RustWrapperContainer.IoPath => $"super::super::{method.ExternSymbol}",
            RustWrapperContainer.ManagedString => method.ExternSymbol,
            RustWrapperContainer.ManagedStringArray => method.ExternSymbol,
            RustWrapperContainer.ManagedIntArray => method.ExternSymbol,
            RustWrapperContainer.ManagedByteArray => method.ExternSymbol,
            RustWrapperContainer.ManagedTimeSpan => method.ExternSymbol,
            RustWrapperContainer.ManagedDateTimeOffset => method.ExternSymbol,
            RustWrapperContainer.ManagedGuid => method.ExternSymbol,
            _ => throw new NotSupportedException($"Rust wrapper container '{method.Container}' is not supported.")
        };
    }

    private static string FormatRustWrapperSuccess(RustWrapperMethod method)
    {
        return method.Result.Kind switch
        {
            RustWrapperResultKind.Boolean => $"Ok({method.ResultVariableName} != 0)",
            RustWrapperResultKind.BooleanAsInt => $"Ok({method.ResultVariableName})",
            RustWrapperResultKind.ObjectHandle => $"Ok({method.Result.RustType}::from_handle({method.ResultVariableName}))",
            RustWrapperResultKind.Scalar => $"Ok({method.ResultVariableName})",
            RustWrapperResultKind.Void => "Ok(())",
            _ => throw new NotSupportedException($"Rust wrapper result kind '{method.Result.Kind}' is not supported.")
        };
    }

