using System.Text;

namespace Rustlyn.Bindings;

public static class PowerShellCmdletShimGenerator
{
    public static string GenerateCSharp(IReadOnlyList<PowerShellCmdletDescriptor> descriptors)
    {
        ArgumentNullException.ThrowIfNull(descriptors);

        var builder = new StringBuilder();
        builder.AppendLine("// @generated by Rustlyn.Bindings. Do not edit by hand.");
        builder.AppendLine("#nullable enable");
        builder.AppendLine("using System.Management.Automation;");
        builder.AppendLine("using Rustlyn.PowerShellSupport;");
        builder.AppendLine();
        builder.AppendLine("namespace Rustlyn.PowerShellCmdlets;");
        builder.AppendLine();

        foreach (var descriptor in descriptors.Where(static descriptor => descriptor.MigrationStrategy == PowerShellCmdletMigrationStrategies.GeneratedRust))
        {
            AppendDescriptor(builder, descriptor);
            builder.AppendLine();
        }

        return builder.ToString();
    }

    private static void AppendDescriptor(StringBuilder builder, PowerShellCmdletDescriptor descriptor)
    {
        var cmdletArguments = descriptor.SupportsShouldProcess
            ? $"{descriptor.VerbExpression}, \"{EscapeString(descriptor.NounName)}\", SupportsShouldProcess = true"
            : $"{descriptor.VerbExpression}, \"{EscapeString(descriptor.NounName)}\"";
        builder.AppendLine($"[Cmdlet({cmdletArguments})]");
        if (descriptor.OutputTypeExpressions.Count > 0)
        {
            builder.AppendLine($"[OutputType({string.Join(", ", descriptor.OutputTypeExpressions.Select(static outputType => $"typeof({outputType})"))})]");
        }

        builder.AppendLine($"public sealed class {descriptor.ClassName} : PSCmdlet");
        builder.AppendLine("{");
        builder.AppendLine("    private readonly PowerShellCmdletCancellation _cancellation = new();");
        builder.AppendLine("    private int _lifecycleStateHandle;");
        builder.AppendLine();

        foreach (var parameter in descriptor.Parameters)
        {
            AppendParameter(builder, parameter);
        }

        AppendLifecycle(builder, "BeginProcessing", descriptor.BeginProcessingEntrypoint, inputExpression: "null", releaseLifecycleState: false);
        AppendLifecycle(builder, "ProcessRecord", descriptor.ProcessRecordEntrypoint, inputExpression: GetPipelineInputExpression(descriptor), releaseLifecycleState: false);
        AppendLifecycle(builder, "EndProcessing", descriptor.EndProcessingEntrypoint, inputExpression: "null", releaseLifecycleState: true);

        builder.AppendLine("    protected override void StopProcessing()");
        builder.AppendLine("        => _cancellation.RequestCancellation();");
        builder.AppendLine();
        builder.AppendLine("    private void InvokeRustEntrypoint(string engineAssemblyName, string typeName, string methodName, object? inputObject, bool checkCancellation = true)");
        builder.AppendLine("    {");
        builder.AppendLine("        var context = new PowerShellCmdletContext(");
        builder.AppendLine("            value => WriteObject(value),");
        builder.AppendLine("            (value, enumerateCollection) => WriteObject(value, enumerateCollection),");
        builder.AppendLine("            WriteVerbose,");
        builder.AppendLine("            WriteWarning,");
        builder.AppendLine("            message => WriteError(CreateErrorRecord(message)),");
        builder.AppendLine("            WriteError,");
        builder.AppendLine("            ThrowTerminatingError,");
        builder.AppendLine("            target => ShouldProcess(target),");
        builder.AppendLine("            (target, action) => ShouldProcess(target, action),");
        builder.AppendLine("            MyInvocation.BoundParameters,");
        builder.AppendLine("            inputObject,");
        builder.AppendLine("            _cancellation,");
        builder.AppendLine("            EnsureLifecycleStateHandle());");
        builder.AppendLine("        PowerShellGeneratedCmdletInvoker.InvokeLifecycle(engineAssemblyName, typeName, methodName, context, checkCancellation);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private int EnsureLifecycleStateHandle()");
        builder.AppendLine("    {");
        builder.AppendLine("        if (_lifecycleStateHandle == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            _lifecycleStateHandle = PowerShellGeneratedCmdletInvoker.CreateLifecycleStateHandle();");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        return _lifecycleStateHandle;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void ReleaseLifecycleState()");
        builder.AppendLine("    {");
        builder.AppendLine("        var lifecycleStateHandle = _lifecycleStateHandle;");
        builder.AppendLine("        if (lifecycleStateHandle == 0)");
        builder.AppendLine("        {");
        builder.AppendLine("            return;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        try");
        builder.AppendLine("        {");
        builder.AppendLine($"            InvokeRustEntrypoint(\"{EscapeString(descriptor.EndProcessingEntrypoint!.EngineAssemblyName)}\", \"{EscapeString(descriptor.EndProcessingEntrypoint.TypeName)}\", \"{EscapeString(GetCleanupMethodName(descriptor))}\", null, checkCancellation: false);");
        builder.AppendLine("        }");
        builder.AppendLine("        finally");
        builder.AppendLine("        {");
        builder.AppendLine("            _lifecycleStateHandle = 0;");
        builder.AppendLine("            PowerShellGeneratedCmdletInvoker.ReleaseLifecycleStateHandle(lifecycleStateHandle);");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private static ErrorRecord CreateErrorRecord(string message)");
        builder.AppendLine("        => new(new RuntimeException(message), \"RustlynPowerShellCmdletError\", ErrorCategory.NotSpecified, null);");
        builder.AppendLine("}");
    }

    private static void AppendParameter(StringBuilder builder, PowerShellCmdletParameterDescriptor parameter)
    {
        builder.Append("    [Parameter");
        var arguments = new List<string>();
        if (parameter.Position is { } position)
        {
            arguments.Add($"Position = {position.ToString(System.Globalization.CultureInfo.InvariantCulture)}");
        }

        if (parameter.ValueFromPipeline)
        {
            arguments.Add("ValueFromPipeline = true");
        }

        if (parameter.ValueFromPipelineByPropertyName)
        {
            arguments.Add("ValueFromPipelineByPropertyName = true");
        }

        if (parameter.Mandatory)
        {
            arguments.Add("Mandatory = true");
        }

        if (arguments.Count > 0)
        {
            builder.Append('(');
            builder.Append(string.Join(", ", arguments));
            builder.Append(')');
        }

        builder.AppendLine("]");

        if (parameter.AllowEmptyString)
        {
            builder.AppendLine("    [AllowEmptyString]");
        }

        if (parameter.ValidateSet is { Count: > 0 })
        {
            builder.AppendLine($"    [ValidateSet({string.Join(", ", parameter.ValidateSet.Select(static value => $"\"{EscapeString(value)}\""))})]");
        }

        var defaultValue = string.IsNullOrWhiteSpace(parameter.DefaultValueExpression)
            ? string.Empty
            : $" = {parameter.DefaultValueExpression};";
        builder.AppendLine($"    public {NormalizeTypeName(parameter.TypeName)} {parameter.Name} {{ get; set; }}{defaultValue}");
        builder.AppendLine();
    }

    private static void AppendLifecycle(StringBuilder builder, string overrideName, PowerShellCmdletEntrypointDescriptor? entrypoint, string inputExpression, bool releaseLifecycleState)
    {
        if (entrypoint is null && !releaseLifecycleState)
        {
            return;
        }

        builder.AppendLine($"    protected override void {overrideName}()");
        if (!releaseLifecycleState)
        {
            builder.AppendLine("    {");
            builder.AppendLine("        try");
            builder.AppendLine("        {");
            builder.AppendLine($"            InvokeRustEntrypoint(\"{EscapeString(entrypoint!.EngineAssemblyName)}\", \"{EscapeString(entrypoint.TypeName)}\", \"{EscapeString(entrypoint.MethodName)}\", {inputExpression});");
            builder.AppendLine("        }");
            builder.AppendLine("        catch");
            builder.AppendLine("        {");
            builder.AppendLine("            ReleaseLifecycleState();");
            builder.AppendLine("            throw;");
            builder.AppendLine("        }");
            builder.AppendLine("    }");
        }
        else
        {
            builder.AppendLine("    {");
            builder.AppendLine("        try");
            builder.AppendLine("        {");
            if (entrypoint is not null)
            {
                builder.AppendLine($"            InvokeRustEntrypoint(\"{EscapeString(entrypoint.EngineAssemblyName)}\", \"{EscapeString(entrypoint.TypeName)}\", \"{EscapeString(entrypoint.MethodName)}\", {inputExpression});");
            }

            builder.AppendLine("        }");
            builder.AppendLine("        finally");
            builder.AppendLine("        {");
            builder.AppendLine("            ReleaseLifecycleState();");
            builder.AppendLine("        }");
            builder.AppendLine("    }");
        }

        builder.AppendLine();
    }

    private static string GetPipelineInputExpression(PowerShellCmdletDescriptor descriptor)
    {
        var pipelineParameter = descriptor.Parameters.FirstOrDefault(static parameter => parameter.ValueFromPipeline);
        return pipelineParameter is null
            ? "null"
            : pipelineParameter.Name;
    }

    private static string GetCleanupMethodName(PowerShellCmdletDescriptor descriptor)
    {
        var processRecordMethodName = descriptor.ProcessRecordEntrypoint?.MethodName
            ?? descriptor.EndProcessingEntrypoint?.MethodName
            ?? throw new InvalidOperationException($"Descriptor '{descriptor.ClassName}' does not define a Rust lifecycle entrypoint.");
        return processRecordMethodName.EndsWith("_process_record", StringComparison.Ordinal)
            ? processRecordMethodName[..^"_process_record".Length] + "_cleanup"
            : processRecordMethodName + "_cleanup";
    }

    private static string NormalizeTypeName(string typeName)
        => typeName == "SwitchParameter" ? "SwitchParameter" : typeName;

    private static string EscapeString(string value)
        => value.Replace("\\", "\\\\", StringComparison.Ordinal).Replace("\"", "\\\"", StringComparison.Ordinal);
}
