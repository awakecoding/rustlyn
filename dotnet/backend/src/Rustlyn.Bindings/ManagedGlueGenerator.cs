using System.Text;

namespace Rustlyn.Bindings;

public static class ManagedGlueGenerator
{
    public static string GenerateRuntimeBridgePartial(BindingSurface surface, string? aliasTablePropertyName = null)
    {
        ArgumentNullException.ThrowIfNull(surface);
        return GenerateRuntimeBridgePartial(BindingManifestFactory.FromSurface(surface), aliasTablePropertyName);
    }

    public static string GenerateRuntimeBridgePartial(BindingManifestDocument document, string? aliasTablePropertyName = null)
    {
        ArgumentNullException.ThrowIfNull(document);
        var builder = new StringBuilder();
        builder.AppendLine("// @generated by Rustlyn.Bindings. Do not edit by hand.");
        builder.AppendLine();
        builder.AppendLine("using System;");
        builder.AppendLine("using System.IO;");
        builder.AppendLine("using Rustlyn.Interop;");
        builder.AppendLine();
        builder.AppendLine("namespace Rustlyn.Backend;");
        builder.AppendLine();
        builder.AppendLine("public static partial class RuntimeBridgeHelpers");
        builder.AppendLine("{");

        if (!string.IsNullOrWhiteSpace(aliasTablePropertyName))
        {
            AppendAliasTable(builder, document, aliasTablePropertyName);
        }

        foreach (var binding in document.Bindings)
        {
            AppendMethod(builder, binding);
        }

        builder.AppendLine();
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static void AppendAliasTable(StringBuilder builder, BindingManifestDocument document, string aliasTablePropertyName)
    {
        builder.Append("    internal static (string Symbol, string HelperMethodName)[] ");
        builder.Append(aliasTablePropertyName);
        builder.AppendLine(" { get; } =");
        builder.AppendLine("    [");
        foreach (var binding in document.Bindings)
        {
            builder.Append("        (\"");
            builder.Append(binding.Symbol);
            builder.Append("\", nameof(");
            builder.Append(binding.Helper);
            builder.AppendLine(")),");
        }

        builder.AppendLine("    ];");
        builder.AppendLine();
    }

    private static void AppendMethod(StringBuilder builder, BindingManifestBinding binding)
    {
        builder.Append("    public static ");
        builder.Append(binding.ReturnType);
        builder.Append(' ');
        builder.Append(binding.Helper);
        builder.Append('(');
        builder.AppendJoin(", ", binding.Parameters.Select(static parameter => $"{parameter.Type} {parameter.Name}"));
        builder.AppendLine(")");
        builder.AppendLine("    {");
        builder.AppendLine("        try");
        builder.AppendLine("        {");

        if (string.Equals(binding.Exception.Kind, "write-exception-out", StringComparison.Ordinal))
        {
            builder.Append("            WriteExceptionOut(");
            builder.Append(binding.Exception.OutParameter);
            builder.AppendLine(", 0);");
        }

        AppendSuccessResult(builder, binding);

        builder.AppendLine("        }");
        builder.AppendLine("        catch (Exception ex)");
        builder.AppendLine("        {");
        AppendCatchResult(builder, binding.Exception);
        builder.AppendLine("        }");
        builder.AppendLine("    }");
    }

    private static void AppendSuccessResult(StringBuilder builder, BindingManifestBinding binding)
    {
        switch (binding.ManagedResultKind)
        {
            case "void-call":
                builder.Append("            ");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine(";");
                builder.AppendLine("            return 0;");
                break;

            case "object-handle":
                builder.Append("            return ManagedInteropRuntime.AddObjectHandle(");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine(");");
                break;

            case "task-handle":
                builder.Append("            return ManagedInteropRuntime.AddTaskHandle(");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine(");");
                break;

            case "value-task-handle":
                builder.Append("            return ManagedInteropRuntime.AddValueTaskHandle(");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine(");");
                break;

            case "int":
            case "long":
            case "float":
            case "double":
                builder.Append("            return ");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine(";");
                break;

            case "boolean-as-int":
                builder.Append("            return ");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine();
                builder.AppendLine("                ? 1");
                builder.AppendLine("                : 0;");
                break;

            case "release-object":
            case "release-exception":
                builder.Append("            ");
                builder.Append(binding.ManagedTarget);
                builder.AppendLine(";");
                builder.AppendLine("            return 0;");
                break;

            default:
                throw new NotSupportedException($"Managed glue result kind '{binding.ManagedResultKind}' is not supported.");
        }
    }

    private static void AppendCatchResult(StringBuilder builder, BindingManifestExceptionConvention exception)
    {
        switch (exception.Kind)
        {
            case "return-exception-handle":
                builder.AppendLine("            return ManagedInteropRuntime.AddExceptionHandle(ex);");
                break;

            case "write-exception-out":
                builder.Append("            WriteExceptionOut(");
                builder.Append(exception.OutParameter);
                builder.AppendLine(", ManagedInteropRuntime.AddExceptionHandle(ex));");
                builder.AppendLine("            return 0;");
                break;

            default:
                throw new NotSupportedException($"Managed glue exception convention '{exception.Kind}' is not supported.");
        }
    }
}
