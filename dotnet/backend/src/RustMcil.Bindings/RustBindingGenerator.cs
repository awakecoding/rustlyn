pub struct ManagedString {
    handle: i32,
}

impl ManagedString {
    fn from_handle(handle: i32) -> Self {
        Self { handle }
    }

    pub fn handle(&self) -> i32 {
        self.handle
    }

    pub fn utf8_len(&self) -> Result<i32, Exception> {
        let mut exception_handle = 0;
        let length = unsafe {
            rust_mcil_bindgen_system_string_utf8_len(self.handle, &mut exception_handle)
        };
        Exception::from_handle(exception_handle)?;
        Ok(length)
    }

    pub fn copy_utf8_into(&self, buffer: &mut [u8]) -> Result<i32, Exception> {
        let mut exception_handle = 0;
        let written = unsafe {
            rust_mcil_bindgen_system_string_copy_utf8(
                self.handle,
                buffer.as_mut_ptr(),
                buffer.len() as i64,
                &mut exception_handle,
            )
        };
        Exception::from_handle(exception_handle)?;
        Ok(written)
    }

    pub fn contains_utf8_parts(&self, needle_ptr: *const u8, needle_len: i64) -> Result<bool, Exception> {
        let mut exception_handle = 0;
        let contains = unsafe {
            rust_mcil_bindgen_system_string_contains_utf8(
                self.handle,
                needle_ptr,
                needle_len,
                &mut exception_handle,
            )
        };
        Exception::from_handle(exception_handle)?;
        Ok(contains != 0)
    }

    pub fn contains(&self, needle: &ManagedString) -> Result<bool, Exception> {
        let mut exception_handle = 0;
        let contains = unsafe {
            rust_mcil_bindgen_system_string_contains_string(
                self.handle,
                needle.handle(),
                &mut exception_handle,
            )
        };
        Exception::from_handle(exception_handle)?;
        Ok(contains != 0)
    }

    pub fn release(self) -> Result<(), Exception> {
        let exception_handle = unsafe { rust_mcil_bindgen_system_object_release(self.handle) };
        Exception::from_handle(exception_handle)
    }
}

