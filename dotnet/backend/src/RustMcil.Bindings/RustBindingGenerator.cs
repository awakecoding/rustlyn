    }

    pub fn get(&self, index: i32) -> Result<ManagedString, Exception> {
        let mut exception_handle = 0;
        let string_handle = unsafe {
            rust_mcil_bindgen_system_string_array_get(self.handle, index, &mut exception_handle)
        };
        Exception::from_handle(exception_handle)?;
        Ok(ManagedString::from_handle(string_handle))
    }

    pub fn release(self) -> Result<(), Exception> {
        let exception_handle = unsafe { rust_mcil_bindgen_system_object_release(self.handle) };
        Exception::from_handle(exception_handle)
    }
}
""";
}