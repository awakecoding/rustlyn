using System.Text;

namespace RustMcil.Bindings;

public static class RustBindingGenerator
{
    public static string GenerateSystemModule(BindingSurface surface)
    {
        ArgumentNullException.ThrowIfNull(surface);

        foreach (var requirement in surface.Requirements)
        {
            requirement.Validate();
        }

        var builder = new StringBuilder();
        builder.AppendLine("// @generated by RustMcil.Bindings. Do not edit by hand.");
        builder.AppendLine();
        builder.AppendLine("unsafe extern \"C\" {");
        foreach (var binding in surface.Externs)
        {
            foreach (var signatureLine in binding.SignatureLines)
            {
                builder.Append("    ");
                builder.AppendLine(signatureLine);
            }
        }

        builder.AppendLine("}");
        builder.AppendLine();
        builder.Append(RustWrapperBody);
        return builder.ToString();
    }

    private const string RustWrapperBody = """
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct Exception {
    handle: i32,
}

impl Exception {
    fn from_handle(handle: i32) -> Result<(), Self> {
        if handle == 0 {
            Ok(())
        } else {
            Err(Self { handle })
        }
    }
}

pub mod console {
    use crate::system::{Exception, ManagedString};

    pub fn write_line_utf8_parts(value_ptr: *const u8, value_len: i64) -> Result<(), Exception> {
        let exception_handle = unsafe {
            super::rust_mcil_bindgen_system_console_write_line_utf8(
                value_ptr,
                value_len,
            )
        };

        Exception::from_handle(exception_handle)
    }

    pub fn write_line(value: &ManagedString) -> Result<(), Exception> {
        let exception_handle = unsafe { super::rust_mcil_bindgen_system_console_write_line_string(value.handle()) };
        Exception::from_handle(exception_handle)
    }
}

pub mod environment {
    use crate::system::{Exception, ManagedStringArray};

    pub fn get_command_line_args() -> Result<ManagedStringArray, Exception> {
        let mut exception_handle = 0;
        let object_handle = unsafe {
            super::rust_mcil_bindgen_system_environment_get_command_line_args(
                &mut exception_handle,
            )
        };

        Exception::from_handle(exception_handle)?;
        Ok(ManagedStringArray::from_handle(object_handle))
    }
}

